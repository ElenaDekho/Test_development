import unittest
from top3 import get_top_3_names


class TestGetTop3Names(unittest.TestCase):

    def setUp(self):
        # Исходные данные из задания
        self.mentors = [
            ["Евгений Шмаргунов", "Олег Булыгин", "Дмитрий Демидов", "Кирилл Табельский", "Александр Ульянцев", "Александр Бардин", "Александр Иванов", "Антон Солонилин", "Максим Филипенко", "Елена Никитина", "Азамат Искаков", "Роман Гордиенко"],
            ["Филипп Воронов", "Анна Юшина", "Иван Бочаров", "Анатолий Корсаков", "Юрий Пеньков", "Илья Сухачев", "Иван Маркитан", "Ринат Бибиков", "Вадим Ерошевичев", "Тимур Сейсембаев", "Максим Батырев", "Никита Шумский", "Алексей Степанов", "Денис Коротков", "Антон Глушков", "Сергей Индюков", "Максим Воронцов", "Евгений Грязнов", "Константин Виролайнен", "Сергей Сердюк", "Павел Дерендяев"],
            ["Евгений Шмаргунов", "Олег Булыгин", "Александр Бардин", "Александр Иванов", "Кирилл Табельский", "Александр Ульянцев", "Роман Гордиенко", "Адилет Асканжоев", "Александр Шлейко", "Алена Батицкая", "Денис Ежков", "Владимир Чебукин", "Эдгар Нуруллин", "Евгений Шек", "Максим Филипенко", "Елена Никитина"],
            ["Владимир Чебукин", "Эдгар Нуруллин", "Евгений Шек", "Валерий Хаслер", "Татьяна Тен", "Александр Фитискин", "Александр Шлейко", "Алена Батицкая", "Александр Беспоясов", "Денис Ежков", "Николай Лопин", "Михаил Ларченко"]
        ]

    def test_get_top_3_names(self):
        result = get_top_3_names(self.mentors)
        expected = "Александр: 10 раз(а), Евгений: 5 раз(а), Максим: 4 раз(а)"
        self.assertEqual(result, expected)

    def test_less_than_3_names(self):
        # Тест с малым количеством имён
        mentors = [
            ["Иван Петров", "Иван Сидоров"],
            ["Анна Иванова"]
        ]
        result = get_top_3_names(mentors)
        expected = "Иван: 2 раз(а), Анна: 1 раз(а)"
        self.assertEqual(result, expected)

    def test_empty_list(self):
        result = get_top_3_names([])
        expected = ""
        self.assertEqual(result, expected)

    def test_all_names_unique(self):
        mentors = [
            ["Анна", "Борис"],
            ["Виктор", "Галина"]
        ]
        result = get_top_3_names(mentors)
        # Берём первые 3 по алфавиту, так как все = 1 раз
        expected = "Анна: 1 раз(а), Борис: 1 раз(а), Виктор: 1 раз(а)"
        self.assertEqual(result, expected)